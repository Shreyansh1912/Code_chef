import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
      Scanner sc = new Scanner(System.in);
      int x = sc.nextInt();
      while(x-->0){
            int t = sc.nextInt();
            if(t<=50) System.out.println("LEFT");
            else System.out.println("RIGHT");
      }
	}
}
