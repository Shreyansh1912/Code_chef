import java.util.*;
import java.lang.*;
import java.io.*;

class Codechef
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		while(t-->0){
		    int k = sc.nextInt();
		    int x = sc.nextInt();
		    System.out.println((k*7)-x);
		}
	}
}
